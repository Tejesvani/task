package Chapter_2;

public class IncOp 
{
	public static void main(String[] args) 
	{
		int a=5;
		System.out.println("a: "+a);
		System.out.println("a++: "+a++);
		System.out.println("a: "+a);
		System.out.println("a--: "+a--);
		System.out.println("a: "+a);
		System.out.println("++a: "+ ++a);
		System.out.println("a: "+a);
		System.out.println("--a: "+--a);
		System.out.println("a: "+a);
	}

}
